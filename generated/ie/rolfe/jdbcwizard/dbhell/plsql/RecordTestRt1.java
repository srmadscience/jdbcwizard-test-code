  /**                                             
  * Unload OUT parameters 
  */                                              
  protected void getStatementResults()  throws CSException 
    {                                                                   
    theLog.debug("Starting to unload data");
    startStatsTimer();
    objectLoader.setBufferSize(bufferSize);
    objectLoader.setKeepFiles(keepFiles);
    
    theParameters.unloadParameters(theCallableStatement); 
    
    // Unload parameter P_OUTCURSOR
    
    theLog.debug("Unloading parameter paramPOutcursor" );
    try
      {
      paramPOutcursor = new com.orindasoft.pub.ReadOnlyRowSet((ResultSet)theParameters.getParam(26), getProcCallStatement(),maxRows, theLog,tempFileDir,keepFiles,tempFilePrefix,tempFileSuffix,keepLobs, useByteArraysForLongsAndLOBS);
      }
    catch (com.orindasoft.pub.CSDBNullObjectException e)
      // A CSDBNullObjectException here  means that a stored procedure or function
      // has returned a null Ref Cursor
      {
      paramPOutcursor = null;
      theLog.debug("Ref Cursor for paramPOutcursor is null. No ReadOnlyRowSet created." );
      }
     
    try 
      { 
      theCallableStatement.clearParameters(); 
      theParameters.clearParameters(); 
      } 
    catch (SQLException e) 
      {
      throw (new CSException("getStatementResults: Unable to clear parameters:" + e.toString())); 
      }
    incRetrieveTime();
    theLog.debug("Finished unloading data");
    }     
            
                                                                          
                                                                          
